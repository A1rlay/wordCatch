ALTER TABLE "QMAnswer" ADD CONSTRAINT "QMAnswer_sessionId_fkey" FOREIGN KEY ("sessionId") REFERENCES "QMSession"("id") ON DELETE CASCADE ON UPDATE CASCADE;
